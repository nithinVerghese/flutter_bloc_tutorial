part of 'increment_bloc.dart';

abstract class IncrementEvent extends Equatable {
  @override
  List<Object?> get props => [];
}

class AddEvent extends IncrementEvent{
  @override
  List<Object?> get props => [];
}

class DecrementEvent extends IncrementEvent{
  @override
  List<Object?> get props => [];
}

class ResetEvent extends IncrementEvent{
  @override
  List<Object?> get props => [];
}
